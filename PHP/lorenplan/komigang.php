<h2>Kom i gang!</h2>
<p>Om du sliter med å komme i gang med bruk av applikasjonen vil du finne en liten guide under! Om du ikke skulle bli tilfredsstilt av informasjonen på denne siden så ta gjerne kontakt.</p>

<h3>Starte applikasjonen</h3>
<p>For å kunne starte applikasjonen må du først ha lastet den ned, og senere trykke ikonet med tittelen "Lorenplan".</p>
<img src="content/komigang/hjelp_1.png" width="256" height="472" /><br>

<h4>Første gang i bruk</h4>
<p>Da du skal starte applikasjonen må du forberede den en smule. Fra applikasjonen sin hovedmeny navigerer du til innstillingene. </p>
<img src="content/komigang/hjelp_2.png"width="256" height="472" /><br>
<p>Ved første øyekast vil du bli blåst vekk av fult av nuller og annet. Dette er en liste over data som applikasjonen har lagret. Siden den nettopp har blitt installert mangler nettopp dette.</p>
<img src="content/komigang/hjelp_3.png"width="256" height="472" /><br>

<h4>Timeplaninnstillinger!</h4>
<p>Dette er siden hvor du vil kunne velge den ønskede uke for timeplanen. Vi kommer tilbake hit senere!</p>
<img src="content/komigang/hjelp_4.png"width="256" height="472" /><br>

<h4>Søkemenyen!</h4>
<p>Bla enda en gang til siden og du vil finne hjertet, lungene eller et annet organ av applikasjonen. Du kan selv bestemme organ, det spiller ingen rolle hvilket. Det er på denne siden du vil søke opp ditt eget navn!</p>
<img src="content/komigang/hjelp_5.png"width="256" height="472" /><br>
<p>Start med å velge skolen du tilhører fra den øverste spinner/dropdownmenyen.</p>
<img src="content/komigang/hjelp_6.png"width="256" height="472" /><br>
<p>Fortsett med å skrive inn deler av navnet ditt eller hele fornavn/etternavn. Anbefaler å unngå søk med fult navn, fornavn og etternavn.</p>
<img src="content/komigang/hjelp_7.png"width="256" height="472" /><br>
<p>Noen skoler bruker kun klassetimeplaner eller har ikke navnene åpent, så om søket gir null treff så prøv å søke etter klassen din istedet! Du vil dukke opp i listen om skolen støtter navn og du er en virkelig person!</p>
<img src="content/komigang/hjelp_8.png"width="256" height="472" /><br>
<p>Tilbake til menyen for å velge uker og annet! Her har vi et valg for hvilken uke du ønsker, oppløsningen på timeplanen og en fiks for enkelte skoler.</p>
<img src="content/komigang/hjelp_9.png"width="256" height="472" /><br>

<h4>Valg av oppløsning!</h4>
<p>Om du velger den høye oppløsningen vil bilde antagelig se ut som det første bilde. Det andre er av den lave oppløsningen.</p>
<img src="content/komigang/hjelp_11.png"width="256" height="472" />
<img src="content/komigang/hjelp_12.png"width="256" height="472" /><br>

<h4>Gul feilmelding!</h4>
<p>Om du skulle være så uheldig å få denne gule ekle errormeldingen når du laster planen din, merk av "Fiks" sjekkboksen!</p>
<img src="content/komigang/hjelp_13.png"width="256" height="472" /><br>

<h4>Lagre innstillingene!</h4>
